export function getServiceBySlug(slug) {
  return serviceCatalog.find((service) => service.slug === slug);
}

export function getServiceByPath(path) {
  return serviceCatalog.find((service) => service.path === path);
}

export function getCategory(serviceSlug, categorySlug) {
  const service = getServiceBySlug(serviceSlug);
  return service?.categories.find((category) => category.slug === categorySlug);
}
